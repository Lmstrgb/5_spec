// Используя Symbol.iterator, создайте объект "Музыкальная коллекция", который можно итерировать. Каждая итерация должна возвращать следующий альбом из коллекции.

// Создайте объект musicCollection, который содержит массив альбомов и имеет свойство-символ Symbol.iterator.

// Каждый альбом имеет следующую структуру:

// {
// title: "Название альбома",
// artist: "Исполнитель",
// year: "Год выпуска"
// }
// Реализуйте кастомный итератор для объекта musicCollection. Итератор должен перебирать альбомы по порядку.

// Используйте цикл for...of для перебора альбомов в музыкальной коллекции и вывода их на консоль в формате:

// Название альбома - Исполнитель (Год выпуска)

// Создаем объект музыкальной коллекции
const musicCollection = {
  albums: [
    { title: "Лето", artist: "Леонид Агутин", year: "2025" },
    { title: "Беспризорники", artist: "гр. Стрелки", year: "2023" },
    { title: "Весёлый", artist: "Лев Лещенко", year: "2009" },
    { title: "Супер", artist: "Юлиана Караулова", year: "2016" },
    { title: "Привет", artist: "Руки вверх", year: "2020" }
  ],

  // Определяем символ для итератора
  [Symbol.iterator]() {
    let index = 0; // Начальный индекс
    const albums = this.albums; // Ссылка на массив альбомов

    return {
      next() {
        if (index < albums.length) {
          // Возвращаем следующий альбом и увеличиваем индекс
          return { value: albums[index++], done: false };
        } else {
          // Если альбомы закончились, возвращаем done: true
          return { done: true };
        }
      }
    };
  }
};

// Используем цикл for...of для перебора альбомов в коллекции
for (const album of musicCollection) {
  console.log(`${album.title} - ${album.artist} (${album.year})`);
}